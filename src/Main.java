public class Main
{
    public static void main(String[] args)
    {
        double summerCost = 200.50;
        double winterCost = 300.3;
        double springCost = 100.75;
        double fallCost = 50.67;
        double TotalYearlyCost;
        TotalYearlyCost = springCost+summerCost+winterCost+fallCost;
        System.out.println("The yearly Maintenance Cost for Summer = " +summerCost);
        System.out.println("The yearly Maintenance Cost for Winter = " +winterCost);
        System.out.println("The yearly Maintenance Cost for Spring = " +springCost);
        System.out.println("The yearly Maintenance Cost for Fall = " +fallCost);
        System.out.println("The Total yearly Maintenance Cost = " +TotalYearlyCost);
    }
}